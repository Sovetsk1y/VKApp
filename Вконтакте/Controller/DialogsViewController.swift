    override func tableView(_ tableView: UITableView, cellForRowAt indexPath: IndexPath) -> UITableViewCell {
        if let cell = tableView.dequeueReusableCell(withIdentifier: "dialogCell", for: indexPath) as? DialogCell {
            let dialog = dialogs[indexPath.row]
            let addressee = dialog.addressee
            cell.dialogImageView.image = UIImage(data: addressee.photoNSData as Data)
            cell.addresseeNameLabel.text = "\(addressee.firstName) \(addressee.lastName)"
//            cell.lastMessageLabel.text = 
            return cell
        } else {
            return UITableViewCell()
        }
